    public ArrayList<Benhnhan> Listbenhnhan()
    {
        ArrayList<Benhnhan> Listbenhnhan = new ArrayList<Benhnhan>();
        Connection conn = DBprovider.ketnoi();
        String query = "SELECT * FROM `tbbenhnhan`";
        Statement st;
        ResultSet rs;
        try
        {
            st = conn.createStatement();
            rs = st.executeQuery(query);
            Benhnhan benhnhan;
            while(rs.next())
            {
                benhnhan = new Benhnhan(rs.getString("mabenhnhan"), rs.getString("tenbenhnhan"), rs.getString("tuoi"), rs.getString("gioitinh"), rs.getString("nghenghiep"), rs.getString("diachi"), rs.getString("sdt"),rs.getString("ghichu"));
                Listbenhnhan.add(benhnhan);
            }
        }
        catch (Exception ex)
        {
            ex.printStackTrace();
        }
        return Listbenhnhan;  
    }
    public void Showbenhnhan()
    {
        ArrayList<Benhnhan> list = Listbenhnhan();
        DefaultTableModel model = (DefaultTableModel)TBbenhnhan.getModel();
        Object[] row = new Object[8];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getmabenhnhan();
            row[1] = list.get(i).gettenbenhnhan();
            row[2] = list.get(i).gettuoi();
            row[3] = list.get(i).getgioitinh();
            row[4] = list.get(i).getnghenghiep();
            row[5] = list.get(i).getdiachi();
            row[6] = list.get(i).getsdt();
            row[7] = list.get(i).getghichu();
            model.addRow(row);
        }
    }

