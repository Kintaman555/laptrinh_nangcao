    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        Connection conn = DBprovider.ketnoi();
        String INSERT = "INSERT INTO tbbenhnhan"
                +"(mabenhnhan,tenbenhnhan,tuoi,gioitinh,nghenghiep,diachi,sdt,ghichu)"
                +"VALUES(?,?,?,?,?,?,?,?)";
        try
        {
            PreparedStatement pst = conn.prepareStatement(INSERT);
            pst.setString(1, txtmabenhnhan.getText());
            pst.setString(2, txttenbenhnhan.getText());
            pst.setString(3, txttuoi.getText());
            pst.setString(4, txtgioitinh.getText());
            pst.setString(5, txtnghenghiep.getText());
            pst.setString(6, txtdiachi.getText());
            pst.setString(7, txtsdt.getText());
            pst.setString(8, txtghichu.getText());
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)TBbenhnhan.getModel();
            model.setRowCount(0);
            Showbenhnhan();
            JOptionPane.showMessageDialog(this, "Thêm thành công !!!");
            
        }
        catch (SQLException ex) 
        {
           JOptionPane.showMessageDialog(this, "Không thêm được !!!");
        }
    }//GEN-LAST:event_jButton13ActionPerformed

